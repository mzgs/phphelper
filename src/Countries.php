    static function alpha2toalpha3(string $code): ?string
    {
        static $map = null;

        $code = strtoupper($code);
        if (strlen($code) !== 2) {
            return null;
        }

        if ($map === null) {
            $map = [];
            foreach (self::$countries as $isoCode => $details) {
                if (strlen($isoCode) === 3) {
                    $map[$details['name']] = $isoCode;
                }
            }
        }

        $country = self::getByCode($code);
        return $country && isset($map[$country['name']]) ? $map[$country['name']] : null;
    }

    static function getAll(): array
    {
        return self::$countries;
    }

    static function getByCode(string $code): ?array
    {
        $code = strtoupper($code);
        return self::$countries[$code] ?? null;
    }

    static function getByName(string $name): ?array
    {
        foreach (self::$countries as $country) {
            if (strtolower($country['name']) === strtolower($name)) {
                return $country;
            }
        }
        return null;
    }

    static function nameWithFlag(string $code): ?string
    {
        $country = self::getByCode($code);
        if ($country) {
            return $country['flag'] . ' ' . $country['name'];
        }
        return null;
    }


